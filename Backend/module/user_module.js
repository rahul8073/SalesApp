//initialize mongoose
const mongoose=require('mongoose');
// create mongodb schema store user information
const userSchema=mongoose.Schema({
    firstName:{
        type:String,
        required:true
    },
    lastName:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true
    },
    password:{
        type:String,
        required:true
    }
})
mongoose.model("userModel",userSchema);